<p class="text__soporte">fig 2. En algunos motores, el filtro está montado en una posición que permite que el 
aceite drene nuevamente al cárter cuando el motor es detenido. Este sería el caso 
del filtro ilustrado en la figura 2, donde está en posición horizontal situado sobre el 
nivel de aceite en el cárter. Dado que el filtro acumula hasta 0.94 litros de aceite, 
toma aproximadamente cinco segundos para que la bomba lo llene después de 
que el motor ha sido puesto en marcha, lapso durante el cual la luz de aviso queda 
encendida.</p>

<p class="text__soporte">Para evitar que el motor funcione sin presión de aceite durante ese período, los 
filtros diseñados para esas posiciones contienen una válvula antidrenaje. Esta 
válvula tiene actualmente la forma de una empacadura la cual permite el libre flujo 
de aceite a través del filtro en la dirección normal, pero cuando el motor esta 
"parado" evita que el aceite se descargue en el cárter. Si la luz de aviso permanece 
encendida durante períodos más prolongados, después de que el filtro ha estado 
usándose en el automotor durante algún tiempo, se recomienda su reemplazo.
</p>

<p class="text__soporte">Bajo algunas condiciones, la luz de aviso puede parpadear cuando el motor está 
funcionando. Esto sucede generalmente cuando el vehículo está doblando o 
situado sobre una cuesta relativamente empinada. Si se produjese un parpadeo 
bajo estas condiciones, se debe a que el nivel de aceite es bajo, y el filtro (malla) de 
la bomba ya no está totalmente sumergido. El nivel de aceite debe ser 
inmediatamente llenado a la situación normal.</p>

