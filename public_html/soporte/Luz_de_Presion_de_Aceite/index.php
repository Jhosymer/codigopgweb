    <p class="parrafo mt-5">Si la luz de aviso permanece encendida durante más de 5 segundos, después de que el motor ha sido puesto en marcha, apáguelo inmediatamente; el nivel de aceite deberá ser verificado y de ser necesario ponerlo en el nivel correcto para el motor en cuestión. Si la luz de aviso continúa encendida cuando el nivel de aceite es normal, entonces podría estar ocurriendo alguna de las siguientes causas:</p>

    <ol class="parrafo mt-5">
            
      <li>El interruptor de presión puede haber fallado.</li>
      <li class="mt-5">El cableado entre el interruptor de presión y la luz de aviso puede estar conectado a tierra.</li>
      <li class="mt-5">La bomba puede haber perdido el cebo debido a una falla, ó la malla de succión (filtro de la bomba) está saturada.</li>
      <li class="mt-5">La válvula reguladora de presión puede estar abierta y retornar el aceite nuevamente al cárter. (Ver la sección: "Problemas con la válvula de regulación de presión de la bomba de aceite").</li>
      <li class="mt-5">La bomba pudo haber atrapado aire en la succión. (Ver la sección: "Pérdida de la alimentación de la bomba de aceite")</li>

    </ol>

    <p class="parrafo mt-5">fig 2. En algunos motores, el filtro está montado en una posición que permite que el aceite drene nuevamente al cárter cuando el motor es detenido. Este sería el caso del filtro ilustrado en la figura 2, donde está en posición horizontal situado sobre el nivel de aceite en el cárter. Dado que el filtro acumula hasta 0.94 litros de aceite, toma aproximadamente cinco segundos para que la bomba lo llene después de que el motor ha sido puesto en marcha, lapso durante el cual la luz de aviso queda encendida. </p>

    <p class="parrafo mt-5">Para evitar que el motor funcione sin presión de aceite durante ese período, los filtros diseñados para esas posiciones contienen una válvula antidrenaje. Esta válvula tiene actualmente la forma de una empacadura la cual permite el libre flujo de aceite a través del filtro en la dirección normal, pero cuando el motor esta "parado" evita que el aceite se descargue en el cárter. Si la luz de aviso permanece encendida durante períodos más prolongados, después de que el filtro ha estado usándose en el automotor durante algún tiempo, se recomienda su reemplazo. </p>

    <p class="parrafo mt-5">Bajo algunas condiciones, la luz de aviso puede parpadear cuando el motor está funcionando. Esto sucede generalmente cuando el vehículo está doblando o situado sobre una cuesta relativamente empinada. Si se produjese un parpadeo bajo estas condiciones, se debe a que el nivel de aceite es bajo, y el filtro (malla) de la bomba ya no está totalmente sumergido. El nivel de aceite debe ser inmediatamente llenado a la situación normal. </p>

