    <h1 class="text__titulo_instruccion resaltar_texto">IMPORTANCIA DEL TESTIGO/LUZ DE NIVEL DE ACEITE</h1>
   
    <p class="text__soporte">La mayor parte de los automotores actuales utilizan una luz de aviso, ubicada 
en el panel de instrumentos para brindar al conductor una señal visual de la 
baja presión de aceite. Es importante tomar en cuenta esta señal, debido a la 
importancia que tiene para la vida del motor un correcto nivel de presión del 
flujo de aceite.
</p>

<p class="text__soporte">La luz de aviso está conectada en serie con un interruptor de presión de aceite, 
como se ilustra en la figura 1. El interruptor de presión contiene contactos 
eléctricos que están en posición cerrada cuando la presión de aceite es inferior 
a 5lbs/pulg2.
 </p>

<p class="text__soporte">Cuando el motor no está funcionando y el encendido está activado, la luz de 
aviso se enciende. Cuando el motor es puesto en marcha, la presión de aceite 
comprime el diafragma en el interruptor de presión, abriendo los contactos y 
haciendo que la luz se apague. </p>


