<p class="text__soporte">Cuando encontramos un elemento filtrante donde ha ocurrido un colapso, la tendencia 
natural es pensar que algo dentro del filtro no funcionó de manera correcta, siendo éste, 
el causante de los daños ocasionados. Generalmente ésta no es la causa de los daños, 
sino el síntoma de un problema en el motor. figura1. Los motores de combustión interna 
incorporan una válvula de alivio entre la entrada y la salida del flujo total de aceite, 
esta válvula permite la continuidad de la lubricación creando un by-pass por donde 
el aceite circula sin pasar por el filtro directamente dentro del motor cuando existe una 
restricción significativa en la presión de aceite, debido a la saturación del filtro o 
el arranque en frío (figura 2). </p>

<p class="text__soporte">Generalmente los fabricantes de motores, han diseñado estas válvulas de alivio para abrir 
cuando la presión diferencial excede las 10 PSI, otros utilizan presiones de hasta 30 PSI. 
Así algunos fabricantes de motores diseñan esta válvula integrada en el monoblock, 
mientras que otros prefieren que sea el filtro el que la lleve. Los filtros automotrices han 
sido diseñados para soportar presiones significativamente superiores a la presión de 
operación normal de trabajo sin colapsarse, de ahí que cuando un filtro sufre un colapso, 
generalmente es el resultado de una obstrucción y de un mal funcionamiento de 
la válvula de alivio. </p>

