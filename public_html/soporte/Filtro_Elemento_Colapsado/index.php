    <p class="parrafo  mt-5">Un filtro colapsado puede ocasionar la pérdida de la filtración y lubricación del motor, si esta condición se presenta, las válvulas reguladora de presión y de alivio deberán ser sustituidas por nuevas tan pronto como sea posible, previendo de esta manera que se presenten daños mayores. El mal funcionamiento de la válvula de alivio y la válvula reguladora de presión y el subsiguiente colapso del filtro, puede no causar daños notables en el motor, pero en ocasiones tiene resultados catastróficos, llegando a producir serios sobrecalentamientos e inclusive daños en bielas por falta de lubricación.</p>


    <p class="subtito_md rojoweb mt-5">Las causas frecuentes que producen el mal funcionamiento tanto de la válvula reguladora de presión como de la válvula de alivio pueden ser:</p>

