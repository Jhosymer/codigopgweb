            <li><span class="li-icon">-</span> Aceites excesivamente contaminados asociados a la condensación, mezcla de refrigerantes y la oxidación del propio aceite</li>
            <li><span class="li-icon">-</span>Descuido del usuario al prolongar los intervalos para el cambio de aceite y filtros recomendados por el fabricante.</li>
            <li><span class="li-icon">-</span> Residuos de carbón arenoso que temporalmente atascan el libre movimiento de los componentes de la válvula.</li>
            <li><span class="li-icon">-</span> Repentinos cambios de aceleración con la presencia de cualquiera de las condiciones arriba mencionadas</li>
           
