    <p class="parrafo  mt-5">Generalmente los fabricantes de motores, han diseñado estas válvulas de alivio para abrir cuando la presión diferencial excede las 10 PSI, otros utilizan presiones de hasta 30 PSI. Así algunos fabricantes de motores diseñan esta válvula integrada en el monoblock, mientras que otros prefieren que sea el filtro el que la lleve. Los filtros automotrices han sido diseñados para soportar presiones significativamente superiores a la presión de operación normal de trabajo sin colapsarse, de ahí que cuando un filtro sufre un colapso, generalmente es el resultado de una obstrucción y de un mal funcionamiento de la válvula de alivio.</p>

