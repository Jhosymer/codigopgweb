    <h1 class="text__titulo_instruccion resaltar_texto">PÉRDIDA DE LA ALIMENTACIÓN DE LA BOMBA DE ACEITE</h1>
   
    <p class="text__soporte">Inmediatamente después de un cambio de aceite, una de las cosas que más confunden y a la que 
se enfrentan muchos mecánicos profesionales o personas que hacen sus propios cambios de 
aceite, es la falta de presión de aceite al arrancar el motor. Aunque no ocurre comúnmente, es 
muy posible que la bomba haya atrapado aire en el lado de la succión (lo que en términos de 
mecánica de fluidos se conoce como pérdida de cebado) durante el cambio de aceite.
</p>
    <p class="text__soporte">Cuando se drena el aceite usado del motor, el aceite pudiera vaciarse también de la tubería de 
succión y posiblemente de la bomba misma. Cuando se agrega el aceite nuevo, la tubería de 
succión que normalmente está sumergida, queda llena de aire. El aire atrapado causa cavitación 
y evita que se establezca el flujo de aceite y la presión requerida. La luz indicadora de baja presión 
de aceite permanecerá encendida o el indicador de presión indicará muy poca presión cuando se 
enciende el motor.
</p>

    <p class="text__soporte">Muchos instaladores tienden a culpar de esta situación al filtro y suponen que el filtro está 
bloqueando el flujo de aceite. Debido a que sospecha del filtro, el instalador colocará un segundo 
filtro. Algunas veces ésto resuelve el problema debido a que el aire atrapado fue liberado cuando 
se removió el primer filtro. Por supuesto, la sospecha del instalador de que el primer filtro estaba 
tapado se incrementa.</p>

    <p class="text__soporte">El punto es que el filtro no era el problema y, en muchos casos, la solución es simple.</p>

    <p class="text__soporte">Si la luz indicadora de baja presión permanece encendida, o el indicador de presión indica muy 
poca o nada de presión después de 30 segundos de encendido el motor, apáguelo. Afloje el filtro 
de aceite hasta que la empacadura toque apenas la base del motor. Desactive el sistema de 
ignición (desconecte el cable de la bobina) para que el motor no encienda. Accione la marcha 
para hacer girar el motor hasta que aparezca aceite por la empacadura. NO ACCIONE LA
MARCHA POR MÁS DE 30 SEGUNDOS. Después de que aparezca aceite por la empacadura, 
apriete el filtro y limpie el exceso de aceite. Arranque el motor. La presión de aceite debe regresar 
al nivel normal en diez segundos.</p>

<ul class="ul_nuemerico_soporte text__soporte">

<li class="li__nuemerico_soporte"> Si el procedimiento anterior falla, será necesario remover el filtro y utilizar una aceitera para 
adicionar aceite de motor dentro del agujero de la base del motor que está adyacente al pin 
roscado donde se enrosca el filtro. Esto cebará la bomba.</li>

<li class="li__nuemerico_soporte">Posteriormente, llene el filtro con aceite, reinstálelo y apriételo. La presión de aceite debe 
regresar a lo normal dentro de los diez segundos después de arrancar el motor.</li>

<li class="li__nuemerico_soporte">El llenar el cárter con aceite inmediatamente después de drenar el aceite usado evitará que 
la bomba atrape aire en la tubería de succión.</li>

<li class="li__nuemerico_soporte">También, asegúrese de que el filtro se instale correctamente. Las instrucciones de 
instalación se encontrarán en el filtro mismo o en la caja de empaque</li>
</ul>
   
