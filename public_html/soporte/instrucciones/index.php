                <ol class="parrafo">
                    <li>Con el aceite del motor a temperatura media (no caliente) coloque un recipiente debajo del cárter. Quite el tapón de drenaje utilizando la herramienta apropiada. Permita que todo el aceite drene del motor.</li>

                    <li class="mt-5">Desenroscar a mano el filtro usado, en algunos casos y dada la incomodidad de la instalación, se requerirá del uso de herramientas especiales que evitarán que el filtro se dañe, haciendo difícil la remoción.</li>

                    <li class="mt-5">Limpiar con un paño limpio la base del filtro, y asegúrese de que la empacadura o junta anterior no se quede pegada a la base. NO USE ESTOPA.</li>

                    <li class="mt-5">Aplicar aceite de motor limpio en la empacadura para facilitar desplazamiento de la junta al montar el filtro. NO UTILIZAR GRASA .</li>

                    <li class="mt-5">Enrosque el filtro nuevo, alineando cuidadosamente las roscas y apriete a mano. No sobre apriete, ni utilice herramienta para apretar.</li>

                    <li class="mt-5">Coloque el tapón al cárter llénelo con aceite limpio hasta el nivel recomendado. Encienda el motor y espere unos segundos a que la presión se normalice. Sí el reloj de presión no indica la normalización o la luz de advertencia en el tablero no se apaga, apague el motor y verifique la instalación</li>

                    <li class="mt-5">Apriete el filtro nuevamente, si es necesario.</li>

                    <li class="mt-5">Apague el motor durante 2 minutos, revise el nivel del aceite y añada aceite si fuese necesario.</li>

                </ol>

            
